    var $rating;
    var $image_file;
    var $date_created;

    //////////////////////////
    //Sets Entity info
    function setRating_scale_id($rating_scale_id) {
        $this->rating_scale_id = $rating_scale_id;
    }

    function setRating($rating) {
        $this->rating = $rating;
    }

    function setImage_file($image_file) {
        $this->image_file = $image_file;
    }

    function setDate_created($date_created) {
        $this->date_created = $date_created;
    }

    //////////////////////////
    //////////////////////////
    //Returns Loaded Entity info
    function getRating_scale_id() {
        return $this->rating_scale_id;
    }

    function getRating() {
        return $this->rating;
    }

    function getImage_file() {
        return $this->image_file;
    }

    function getDate_created() {
        return $this->date_created;
    }

}

