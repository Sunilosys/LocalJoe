<?php

class Application_Model_LjCity extends Application_Model_LjBaseModel {

    //Modlular level variables
    protected $table_name = "city";
    protected $table_pk = "city_id";
    protected $class_name = 'Application_Model_LjCity';
    var $city_id;
    var $region_id;
    var $state_id;
    var $city;
    var $date_created;

    //////////////////////////
    //Sets Entity info
    function setCity_id($city_id) {
        $this->city_id = $city_id;
    }

    function setRegion_id($region_id) {
        $this->region_id = $region_id;
    }

    function setState_id($state_id) {
        $this->state_id = $state_id;
    }

    function setCity($city) {
        $this->city = $city;
    }

    function setDate_created($date_created) {
        $this->date_created = $date_created;
    }

    //////////////////////////
    //////////////////////////
    //Returns Loaded Entity info
    function getCity_id() {
        return $this->city_id;
    }

    function getRegion_id() {
        return $this->region_id;
    }

    function getState_id() {
        return $this->state_id;
    }

    function getCity() {
        return $this->city;
    }

    function getDate_created() {
        return $this->date_created;
    }

}

//End class

